export function modelPageName(pgName) {
    //console.log(pgName);

    //$("#app").html(eval(pgName));

    if(pgName == "homeContent") {
        $("nav a").removeClass("black-text").addClass("white-text");
        $(".homeNav").css("border-block-color", "#fff");
        $("#Rectangle_6").css("stroke", "#fff");
        $("#eventsy").css("fill", "#fff");
        } else {
        $("nav a").removeClass("white-text").addClass("black-text");
        $(".homeNav").css("border-block-color", "#000");
        $("#Rectangle_6").css("stroke", "#000");
        $("#eventsy").css("fill", "#000");
        }

    
    
    try {
        eval(pgName);
        $("#app").html(eval(pgName));
    } catch (e) {
        console.log("hello", e);
    }
}